// agent_info.go implements the `agent-info` subcommand (#1051): a
// self-describing capability + runtime-state dump so LLM agents operating
// go-trader can discover the binary's surface from the binary itself instead
// of hand-maintained docs that inevitably drift.
//
// Design notes (validated against the codebase, see #1051):
//   - There is NO cobra/command framework. Capabilities are sourced from the
//     curated agentInfoCommands registry below; a test asserts it covers every
//     knownSubcommands entry so a new subcommand without a doc entry fails CI.
//   - Config schema is reflected from the Config struct (top-level keys, Go
//     type, json name, optionality). Per-key prose descriptions/defaults are
//     not reflectable and are deliberately omitted rather than faked.
//   - Env vars are a curated registry; a test cross-checks it against every
//     os.Getenv("...") literal in scheduler/*.go so the security-sensitive
//     surface can't silently grow stale.
//   - State-DB schema + live state are read from the SQLite file READ-ONLY
//     (no migrations, no writes) so running agent-info never mutates state or
//     races the live daemon. Live state is a DB snapshot and is labeled as
//     such — the running daemon's in-memory state is authoritative; agents
//     wanting live truth should query the status API (server.go, port 8099).
//   - --bootstrap-md writes AGENTS.generated.md, NEVER AGENTS.md (which is a
//     symlink to the hand-maintained CLAUDE.md — writing it would clobber it).

// agentInfoGeneratedFile is the default markdown target. Deliberately distinct
// from AGENTS.md (a symlink to CLAUDE.md): the binary owns this file, humans
// own CLAUDE.md.
const agentInfoGeneratedFile = "AGENTS.generated.md"

// agentInfoMarkdownHeader marks the file as machine-owned so humans don't edit
// it by hand (their edits would be overwritten on the next update).
const agentInfoMarkdownHeader = "<!-- AUTO-GENERATED by `go-trader agent-info`. Do not edit by hand; edits are overwritten on update. Human notes belong in CLAUDE.md. -->"

