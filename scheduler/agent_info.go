func runAgentInfo(args []string) int {
	fs := flag.NewFlagSet("agent-info", flag.ContinueOnError)
	configPath := fs.String("config", "scheduler/config.json", "Path to config file")
	bootstrapMD := fs.Bool("bootstrap-md", false, "Write/refresh "+agentInfoGeneratedFile+" instead of dumping JSON to stdout")
	appendChangelog := fs.Bool("append-changelog", false, "When writing markdown, append a version-stamped changelog entry")
	output := fs.String("output", agentInfoGeneratedFile, "Markdown output path (with --bootstrap-md)")
	if err := fs.Parse(args); err != nil {
		return 2
	}

	// LoadConfig emits [INFO] default-application lines via fmt.Printf (stdout).
	// The default JSON dump must keep stdout pure, so route those to stderr by
	// temporarily redirecting os.Stdout across the load. The swap is wrapped in a
	// closure with a deferred restore so a panic inside the load (e.g. a nil deref
	// in a migration path) cannot leave os.Stdout pointing at stderr for the rest
	// of the process; the closure also restores stdout before the JSON dump below.
	cfg, err := func() (*Config, error) {
		realStdout := os.Stdout
		defer func() { os.Stdout = realStdout }()
		os.Stdout = os.Stderr
		return loadConfigSnapshot(*configPath)
	}()
	if err != nil {
		// Config is optional for agent-info: capabilities/schema/env are still
		// useful without it. Warn, continue with a nil config.
		fmt.Fprintf(os.Stderr, "agent-info: warning: could not load config %s: %v\n", *configPath, err)
		cfg = nil
	}

	info := buildAgentInfo(cfg, Version, time.Now().UTC())

	if *bootstrapMD {
		md := renderAgentInfoMarkdown(info)
		if err := writeAgentInfoMarkdown(*output, md, *appendChangelog, info, time.Now().UTC()); err != nil {
			fmt.Fprintf(os.Stderr, "agent-info: failed to write %s: %v\n", *output, err)
			return 1
		}
		fmt.Fprintf(os.Stderr, "agent-info: wrote %s\n", *output)
		return 0
	}

	enc := json.NewEncoder(os.Stdout)
	enc.SetIndent("", "  ")
	if err := enc.Encode(info); err != nil {
		fmt.Fprintf(os.Stderr, "agent-info: failed to encode JSON: %v\n", err)
		return 1
	}
	return 0
}

// loadConfigSnapshot loads the config WITHOUT mutating the on-disk file.
// LoadConfig runs MigrateConfig, which rewrites pre-v13/pre-v15 configs back to
// disk — unacceptable for a read-only introspection command. Loading a throwaway
// copy keeps any migration write on the temp file so the operator's config is
// never touched, while still reflecting the effective (migrated, defaulted) shape.
func loadConfigSnapshot(path string) (*Config, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, fmt.Errorf("read config: %w", err)
	}
	tmp, err := os.CreateTemp("", "agent-info-cfg-*.json")
	if err != nil {
		return nil, err
	}
	tmpPath := tmp.Name()
	defer os.Remove(tmpPath)
	if _, err := tmp.Write(data); err != nil {
		tmp.Close()
		return nil, err
	}
	if err := tmp.Close(); err != nil {
		return nil, err
	}
	return LoadConfig(tmpPath)
}

// buildAgentInfo assembles the report. Pure given (cfg, version, now) plus a
// read-only DB read keyed off cfg.DBFile; with a nil cfg the DB sections are
// empty and the static sections are still populated.
func buildAgentInfo(cfg *Config, version string, now time.Time) agentInfo {
	info := agentInfo{
		Version:      version,
		GeneratedAt:  now.Format(time.RFC3339),
		Capabilities: agentInfoCommands,
		ConfigSchema: reflectConfigSchema(),
		EnvVars:      resolveEnvVarPresence(agentInfoEnvVars),
		LiveState:    agentLiveState{Source: "none", Note: "no config loaded"},
	}

	if cfg != nil {
		info.Strategies = summarizeStrategies(cfg)
		dbPath := cfg.DBFile
		if dbPath == "" {
			dbPath = "scheduler/state.db"
		}
		statusPort := cfg.StatusPort
		if statusPort == 0 {
			statusPort = DefaultStatusPort
		}
		info.StateDB, info.LiveState = readStateDBReadOnly(dbPath, statusPort)
	}
	return info
}

// reflectConfigSchema reflects the top-level Config struct keys.
func reflectConfigSchema() []agentConfigField {
	t := reflect.TypeOf(Config{})
	var out []agentConfigField
	for i := 0; i < t.NumField(); i++ {
		f := t.Field(i)
		tag := f.Tag.Get("json")
		if tag == "" || tag == "-" {
			continue // unexported-from-JSON (e.g. StatusToken `json:"-"`)
		}
		parts := strings.Split(tag, ",")
		name := parts[0]
		if name == "" {
			continue
		}
		optional := false
		for _, p := range parts[1:] {
			if p == "omitempty" {
				optional = true
			}
		}
		out = append(out, agentConfigField{JSONName: name, GoType: f.Type.String(), Optional: optional})
	}
	return out
}

// resolveEnvVarPresence stamps whether each registered env var is currently set
// (never echoing the value — secrets stay secret).
func resolveEnvVarPresence(vars []agentEnvVar) []agentEnvVar {
	out := make([]agentEnvVar, len(vars))
	for i, v := range vars {
		v.Set = os.Getenv(v.Name) != ""
		out[i] = v
	}
	return out
}

// summarizeStrategies derives the open/close decision modules per strategy.
func summarizeStrategies(cfg *Config) []agentStrategyInfo {
	out := make([]agentStrategyInfo, 0, len(cfg.Strategies))
	for _, sc := range cfg.Strategies {
		closeModule := "(open-as-close)"
		if sc.CloseStrategy != nil && sc.CloseStrategy.Name != "" {
			closeModule = sc.CloseStrategy.Name
		}
		openModule := sc.OpenStrategy.Name
		if openModule == "" {
			openModule = sc.Script
		}
		out = append(out, agentStrategyInfo{
			ID:               sc.ID,
			Type:             sc.Type,
			Platform:         sc.Platform,
			Symbol:           sc.Symbol,
			OpenModule:       openModule,
			CloseModule:      closeModule,
			AllowedRegimes:   sc.AllowedRegimes,
			RegimeGateWindow: sc.RegimeGateWindow,
		})
	}
	sort.Slice(out, func(i, j int) bool { return out[i].ID < out[j].ID })
	return out
}

// readStateDBReadOnly introspects the schema and a live-state snapshot from the
// SQLite DB WITHOUT migrating or writing. A missing/locked DB degrades to an
// empty schema and DBPresent=false rather than erroring the whole command.
func readStateDBReadOnly(path string, statusPort int) ([]agentTable, agentLiveState) {
	live := agentLiveState{
		Source: "state.db snapshot",
		Note:   fmt.Sprintf("snapshot read from %s; may lag the running daemon. Query the status API on port %d for authoritative live state.", path, statusPort),
	}
	if _, err := os.Stat(path); err != nil {
		live.DBPresent = false
		live.Note = fmt.Sprintf("state DB %s not present yet; %s", path, live.Note)
		return nil, live
	}
	// Read-only DSN: no schema creation, no migration, no WAL writes.
	db, err := sql.Open("sqlite", "file:"+path+"?mode=ro&_pragma=busy_timeout(5000)")
	if err != nil {
		live.DBPresent = false
		return nil, live
	}
	defer db.Close()

	tables, err := readDBSchema(db)
	if err != nil {
		live.DBPresent = false
		return nil, live
	}
	positions, err := readOpenPositions(db)
	if err != nil {
		// A read that failed mid-iteration could under-report open exposure; mark
		// the snapshot untrustworthy rather than hand back a partial list that
		// looks complete. Schema is still returned (it read cleanly).
		live.DBPresent = false
		live.Note = fmt.Sprintf("state DB %s present but open-position read failed (%v) — do not trust this snapshot; query the status API on port %d. %s", path, err, statusPort, live.Note)
		return tables, live
	}
	// option_positions is a separate class; omitting it would read as zero
	// exposure for an options account. Same fail-closed contract as above.
	optionPositions, err := readOpenOptionPositions(db)
	if err != nil {
		live.DBPresent = false
		live.Note = fmt.Sprintf("state DB %s present but option-position read failed (%v) — do not trust this snapshot; query the status API on port %d. %s", path, err, statusPort, live.Note)
		return tables, live
	}
	live.DBPresent = true
	live.CycleCount = readCycleCount(db)
	live.OpenPositions = positions
	live.OpenOptionPositions = optionPositions
	return tables, live
}

func readDBSchema(db *sql.DB) ([]agentTable, error) {
	rows, err := db.Query(`SELECT name FROM sqlite_master WHERE type='table' AND name NOT LIKE 'sqlite_%' ORDER BY name`)
	if err != nil {
		return nil, err
	}
	var names []string
	for rows.Next() {
		var n string
		if err := rows.Scan(&n); err != nil {
			rows.Close()
			return nil, err
		}
		names = append(names, n)
	}
	rows.Close()
	if err := rows.Err(); err != nil {
		return nil, err
	}

	var tables []agentTable
	for _, name := range names {
		cols, err := readTableColumns(db, name)
		if err != nil {
			return nil, err
		}
		tables = append(tables, agentTable{Name: name, Columns: cols})
	}
	return tables, nil
}

func readTableColumns(db *sql.DB, table string) ([]agentTableColumn, error) {
	// table comes from sqlite_master, not user input; still, PRAGMA can't be
	// parameterized so quote-escape defensively.
	rows, err := db.Query(fmt.Sprintf("PRAGMA table_info(%q)", table))
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var cols []agentTableColumn
	for rows.Next() {
		var (
			cid       int
			name      string
			ctype     string
			notnull   int
			dfltValue sql.NullString
			pk        int
		)
		if err := rows.Scan(&cid, &name, &ctype, &notnull, &dfltValue, &pk); err != nil {
			return nil, err
		}
		cols = append(cols, agentTableColumn{Name: name, Type: ctype, NotNull: notnull != 0, PK: pk != 0})
	}
	return cols, rows.Err()
}

func readCycleCount(db *sql.DB) int64 {
	var c int64
	// app_state is a singleton row (id=1).
	_ = db.QueryRow(`SELECT cycle_count FROM app_state WHERE id=1`).Scan(&c)
	return c
}

func readOpenPositions(db *sql.DB) ([]agentOpenPosition, error) {
	rows, err := db.Query(`SELECT strategy_id, symbol, side, quantity, avg_cost, regime FROM positions ORDER BY strategy_id, symbol`)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var out []agentOpenPosition
	for rows.Next() {
		var p agentOpenPosition
		if err := rows.Scan(&p.StrategyID, &p.Symbol, &p.Side, &p.Quantity, &p.AvgCost, &p.Regime); err != nil {
			// A mid-scan failure must not return a silently truncated slice that
			// reads as a complete snapshot — signal the error so the caller can
			// mark the live state untrustworthy rather than under-report exposure.
			return nil, err
		}
		out = append(out, p)
	}
	return out, rows.Err()
}

func readOpenOptionPositions(db *sql.DB) ([]agentOpenOptionPosition, error) {
	rows, err := db.Query(`SELECT strategy_id, underlying, option_type, strike, expiry, action, quantity FROM option_positions ORDER BY strategy_id, underlying, expiry`)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var out []agentOpenOptionPosition
	for rows.Next() {
		var p agentOpenOptionPosition
		if err := rows.Scan(&p.StrategyID, &p.Underlying, &p.OptionType, &p.Strike, &p.Expiry, &p.Action, &p.Quantity); err != nil {
			// Same fail-closed contract as readOpenPositions: never return a
			// silently truncated slice that reads as a complete snapshot.
			return nil, err
		}
		out = append(out, p)
	}
	return out, rows.Err()
}

// renderAgentInfoMarkdown renders the report as agent-readable markdown.
func renderAgentInfoMarkdown(info agentInfo) string {
	var b strings.Builder
	b.WriteString(agentInfoMarkdownHeader)
	b.WriteString("\n\n# go-trader — agent capabilities\n\n")
	fmt.Fprintf(&b, "Version: `%s`  ·  Generated: %s\n\n", info.Version, info.GeneratedAt)

	b.WriteString("## Capabilities (CLI subcommands)\n\n")
	for _, c := range info.Capabilities {
		fmt.Fprintf(&b, "- **%s** — %s\n  - `%s`\n", c.Name, c.Summary, c.Usage)
	}

	b.WriteString("\n## Config schema (`config.json` top-level keys)\n\n")
	b.WriteString("| key | type | optional |\n|---|---|---|\n")
	for _, f := range info.ConfigSchema {
		fmt.Fprintf(&b, "| `%s` | `%s` | %v |\n", f.JSONName, f.GoType, f.Optional)
	}

	b.WriteString("\n## Environment variables\n\n")
	b.WriteString("| var | secret | purpose |\n|---|---|---|\n")
	for _, v := range info.EnvVars {
		fmt.Fprintf(&b, "| `%s` | %v | %s |\n", v.Name, v.Secret, v.Purpose)
	}

	b.WriteString("\n## State DB schema\n\n")
	if len(info.StateDB) == 0 {
		b.WriteString("_No state DB found (it is created on first daemon run)._\n")
	} else {
		for _, t := range info.StateDB {
			fmt.Fprintf(&b, "- **%s**: ", t.Name)
			names := make([]string, 0, len(t.Columns))
			for _, c := range t.Columns {
				names = append(names, c.Name)
			}
			b.WriteString("`" + strings.Join(names, "`, `") + "`\n")
		}
	}

	b.WriteString("\n## Strategies (open/close decision modules)\n\n")
	if len(info.Strategies) == 0 {
		b.WriteString("_No strategies configured._\n")
	} else {
		b.WriteString("| id | type | platform | open | close | regime gate |\n|---|---|---|---|---|---|\n")
		for _, s := range info.Strategies {
			fmt.Fprintf(&b, "| `%s` | %s | %s | `%s` | `%s` | %s |\n",
				s.ID, s.Type, s.Platform, s.OpenModule, s.CloseModule, strings.Join(s.AllowedRegimes, ","))
		}
	}

	b.WriteString("\n## Live state (DB snapshot)\n\n")
	fmt.Fprintf(&b, "%s\n\n", info.LiveState.Note)
	if info.LiveState.DBPresent {
		fmt.Fprintf(&b, "Cycle count: %d  ·  Open positions: %d (perps/spot/futures) + %d option legs\n",
			info.LiveState.CycleCount, len(info.LiveState.OpenPositions), len(info.LiveState.OpenOptionPositions))
	}

	return b.String()
}

// agentInfoChangelogMaxEntries bounds the retained changelog history so the
// auto-generated file (refreshed on every deploy via update.sh) cannot grow
// without limit; ~50 entries is roughly two months of daily deploys.
const agentInfoChangelogMaxEntries = 50

// writeAgentInfoMarkdown writes (or refreshes) the markdown file. Any existing
// Changelog block is preserved across regenerations regardless of mode — a bare
// refresh never silently drops history. With appendChangelog, a new
// version-stamped entry is prepended to that history. The retained history is
// capped at agentInfoChangelogMaxEntries (newest kept).
func writeAgentInfoMarkdown(path, md string, appendChangelog bool, info agentInfo, now time.Time) error {
	const changelogMarker = "## Changelog\n"

	// Collect prior entries (newest-first) from any existing changelog block so
	// the history survives a refresh in either mode. Without this, a bare
	// --bootstrap-md would overwrite prior history; history only changes on
	// explicit opt-in (appendChangelog) or by aging past the retention cap.
	var entries []string
	if prev, err := os.ReadFile(path); err == nil {
		if idx := strings.Index(string(prev), changelogMarker); idx >= 0 {
			for _, line := range strings.Split(string(prev)[idx+len(changelogMarker):], "\n") {
				if strings.HasPrefix(strings.TrimSpace(line), "- ") {
					entries = append(entries, strings.TrimRight(line, "\r"))
				}
			}
		}
	}

	if appendChangelog {
		entry := fmt.Sprintf("- %s — version `%s` (%d strategies, %d open positions)",
			now.Format("2006-01-02"), info.Version, len(info.Strategies), len(info.LiveState.OpenPositions))
		entries = append([]string{entry}, entries...)
	}

	// Bound the file: keep only the newest entries.
	if len(entries) > agentInfoChangelogMaxEntries {
		entries = entries[:agentInfoChangelogMaxEntries]
	}

	// Nothing to carry forward and no new entry requested: plain render.
	if len(entries) == 0 {
		return os.WriteFile(path, []byte(md), 0644)
	}

	var b strings.Builder
	b.WriteString(md)
	b.WriteString("\n")
	b.WriteString(changelogMarker)
	b.WriteString("\n")
	for _, e := range entries {
		b.WriteString(e)
		b.WriteString("\n")
	}
	return os.WriteFile(path, []byte(b.String()), 0644)
}
